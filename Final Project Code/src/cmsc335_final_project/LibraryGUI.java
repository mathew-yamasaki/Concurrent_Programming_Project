    /** make appropriate methods call for a selected radio button after user strikes 'Enter' key */
    private void searchFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchFieldActionPerformed
       
        if (bookIndexSearchRadioButton.isSelected()) {
            try {
                _jBookIndex = Integer.parseInt(this.searchField.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Please enter a numeric value",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            searchResultsDisplay.setText(l.bookMapIterator(Library.bookMap, _jBookIndex));
        }
        if (titleSearchRadioButton.isSelected()) {
            _jBookTitle = this.searchField.getText();
            searchResultsDisplay.setText(l.getInfoFromTitle(Library.books, 
                    Library.authors, _jBookTitle));
        }
        if (genreSearchRadioButton.isSelected()) {
            _jGenre = this.searchField.getText();
            l.getTitleFromGenre(Library.books, _jGenre, searchResultsDisplay);
        }
        if (authorIndexSearchRadioButton.isSelected()) {
            try {
            _jAuthorIndex = Integer.parseInt(this.searchField.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Please enter a number",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            searchResultsDisplay.setText(l.authorMapIterator(l.authorMap, _jAuthorIndex));
        }
    }//GEN-LAST:event_searchFieldActionPerformed

